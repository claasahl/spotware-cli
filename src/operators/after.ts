export function after(
  payloadType: $.ProtoOAPayloadType | $.ProtoPayloadType
): OperatorFunction<$.ProtoMessages, $.ProtoMessages> {
  return pipe(
    filter((pm): pm is $.ProtoMessages => pm.payloadType === payloadType),
    first()
  );
}
